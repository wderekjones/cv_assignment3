function A = get_A(x, X)
n = size(x, 1);
for h = 1:2:n
    A(h,:) = [X(h,1) X(h,2) X(h,3) 1 ...
        0 0 0 0 ...
        -x(h,1)*X(h,1) -x(h,1)*X(h,2) ...
        -x(h,1)*X(h,3) -x(h,1)];

    A(h+1,:) = [0 0 0 0 ...
        X(h,1) X(h,2) X(h,3) 1 ...
        -x(h,2)*X(h,1) -x(h,2)*X(h,2) ...
        -x(h,2)*X(h,3) -x(h,2)];
end
